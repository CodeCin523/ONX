#ifndef __ONX_ID_H__
#define __ONX_ID_H__

#include "onx_num.h"

typedef u16 onx_id16_t;
enum {
    ONX_ID16_GEN = 0b0000'0000'00000000,
    ONX_ID16_TKN = 0b1000'0000'00000000,
    ONX_ID16_EVT = 0b0100'0000'00000000,
    ONX_ID16_ACT = 0b1100'0000'00000000,
    ONX_ID16_HND = 0b0010'0000'00000000,
};
typedef u32 onx_id32_t;
enum {
    ONX_ID32_GEN = (ONX_ID16_GEN << 15),
    ONX_ID32_TKN = (ONX_ID16_TKN << 15),
    ONX_ID32_EVT = (ONX_ID16_EVT << 15),
    ONX_ID32_ACT = (ONX_ID16_ACT << 15),
    ONX_ID32_HND = (ONX_ID16_HND << 15),
};

typedef u16 onx_hid16_t;
enum {
    ONX_HID16_CORE             = (ONX_ID16_HND | 0b0000'00000000),
    ONX_HID16_LOGGER           = (ONX_ID16_HND | 0b0000'00000001),
    ONX_HID16_THREAD           = (ONX_ID16_HND | 0b0000'00000010),
    ONX_HID16_WINDOW           = (ONX_ID16_HND | 0b0000'00000011),
    ONX_HID16_VULKAN_INSTANCE  = (ONX_ID16_HND | 0b0001'00000000),
    ONX_HID16_VULKAN_DEVICES   = (ONX_ID16_HND | 0b0001'00000001),
    ONX_HID16_VULKAN_SWAPCHAIN = (ONX_ID16_HND | 0b0001'00000010),
};
typedef u32 onx_hid32_t;
enum {
    ONX_HID32_CORE             = (ONX_HID16_CORE               << 15),
    ONX_HID32_LOGGER           = (ONX_HID16_LOGGER             << 15),
    ONX_HID32_THREAD           = (ONX_HID16_THREAD             << 15),
    ONX_HID32_WINDOW           = (ONX_HID16_WINDOW             << 15),
    ONX_HID32_VULKAN_INSTANCE  = (ONX_HID16_VULKAN_INSTANCE    << 15),
    ONX_HID32_VULKAN_DEVICES   = (ONX_HID16_VULKAN_DEVICES     << 15),
    ONX_HID32_VULKAN_SWAPCHAIN = (ONX_HID16_VULKAN_SWAPCHAIN   << 15),
};

#endif /* __ONX_ID_H__ */